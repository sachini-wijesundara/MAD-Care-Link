  @override
  void initState() {
    pinCode = Random().nextInt(9999 - 1000) + 1000;
    print('Generated Pin Code: $pinCode');
    super.initState();
  }

  Future<void> sendEmail() async {
    DateTime currentTime = DateTime.now();
    DateTime futureTime = currentTime.add(const Duration(minutes: 15));
    String date =
        "${futureTime.year}-${futureTime.month.toString().padLeft(2, '0')}-${futureTime.day.toString().padLeft(2, '0')}";
    String hour = (futureTime.hour > 12
            ? futureTime.hour - 12
            : futureTime.hour)
        .toString()
        .padLeft(2, '0');
    String minute = futureTime.minute.toString().padLeft(2, '0');
    String period = futureTime.hour >= 12 ? 'PM' : 'AM';
    String formattedDateTime = "$date $hour:$minute $period";

    final serviceId = 'service_ou0qtd8';
    final templateId = 'template_5mt9wem';
    final userId = 'M4Yu0OH0XvpV93rRZ';
    final url = Uri.parse('https://api.emailjs.com/api/v1.0/email/send');

    try {
      final reqBody = json.encode({
        'service_id': serviceId,
        'template_id': templateId,
        'user_id': userId,
        'template_params': {
          "passcode": "$pinCode",
          "time": formattedDateTime,
          "email": _emailController.text,
        },
      });

      print('Request Payload: $reqBody');

      final response = await http.post(
        url,
        headers: {
          'origin': 'http://localhost',
          'Content-Type': 'application/json',
        },
        body: reqBody,
      );

      print('Response Status: ${response.statusCode}');
      print('Response Body: ${response.body}');

      if (response.statusCode == 200) {
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
              content: Text('Email Sent Successfully!'),
              backgroundColor: Colors.green,
            ),
          );

          _nameController.clear();
          _emailController.clear();
        }
      } else {
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text('Failed to Send Email: ${response.body}'),
              backgroundColor: Colors.red,
            ),
          );
        }
      }
    } catch (e) {
      print('Exception during API call: $e');
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Error: $e'), backgroundColor: Colors.red),
        );
      }
    }
  }

  void _register() async {
    if (_formKey.currentState!.validate()) {
      sendEmail();
      Navigator.of(context).push(
        MaterialPageRoute(
          builder:
              (context) => FourDigitsCodeScreen(
                name: _nameController.text,
                contactNumber: _contactNumberController.text,
                email: _emailController.text,
                password: _passwordController.text,
                code: pinCode,
              ),
        ),
      );

      return;
    }
  }

